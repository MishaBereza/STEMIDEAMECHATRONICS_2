def get_text(key, lang='en', **kwargs):
    if lang not in TRANSLATIONS:
        lang = 'en'
    text = TRANSLATIONS[lang].get(key, TRANSLATIONS['en'].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text
